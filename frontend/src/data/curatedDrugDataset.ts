/**
 * Normalize drug name for deduplication
 * Removes case, whitespace, and punctuation differences
 */
export function normalizeDrugName(name: string): string {
  return name
    .toLowerCase()
    .trim()
    .replace(/[^\w\s]/g, '') // Remove punctuation
    .replace(/\s+/g, ' '); // Normalize whitespace
}

/**
 * Deduplicate drugs by normalized name
 * When duplicates exist, prefer: banned > approved, newer date > older date
 * Deterministic and stable tie-breaking
 */
export function deduplicateDrugs(drugs: Drug[]): Drug[] {
  const drugMap = new Map<string, Drug>();

  // Sort for deterministic processing
  const sortedDrugs = [...drugs].sort((a, b) => a.name.localeCompare(b.name));

  for (const drug of sortedDrugs) {
    const normalizedName = normalizeDrugName(drug.name);
    const existing = drugMap.get(normalizedName);

    if (!existing) {
      drugMap.set(normalizedName, drug);
    } else {
      // Prefer banned over approved
      if (drug.status === DrugStatus.banned && existing.status === DrugStatus.approved) {
        drugMap.set(normalizedName, drug);
      } else if (drug.status === existing.status) {
        // If same status, prefer newer date (deterministic)
        if (drug.date > existing.date) {
          drugMap.set(normalizedName, drug);
        }
      }
    }
  }

  return Array.from(drugMap.values());
}

